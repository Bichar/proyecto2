    private void calculaTotales() {

        DefaultTableModel model = (DefaultTableModel) tableDetalle.getModel();

        Object[] columnData = new Object[tableDetalle.getRowCount()];  // One entry for each row
        Object[] rowData = new Object[tableDetalle.getRowCount()];

        Float subtotal = 0f;
        //recorre la tabla y suma la columna 5 y lo acumula en la var subtotal
        for (int i = 0; i < tableDetalle.getRowCount(); i++) {  // Loop through the rows
            subtotal += Float.valueOf(tableDetalle.getValueAt(i, 5).toString());
        }

        System.out.println("subtotal: " + String.valueOf(subtotal));
        System.out.println("subtotal: " + String.valueOf(subtotal * 0.16));
        System.out.println("subtotal: " + String.valueOf(subtotal * 1.16));

        this.subtotal.setText(String.valueOf(subtotal));
        this.iva.setText(String.valueOf(Math.round(subtotal * 0.16)));
        this.total.setText(String.valueOf(Math.round(subtotal * 1.16)));

    }


