    private void agregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarProductoActionPerformed
        // TODO add your handling code here:
        Producto p = new Producto();
        AgregaProducto ap = new AgregaProducto(this.main, true, p);

        ap.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
               
                DefaultTableModel model = (DefaultTableModel) tableDetalle.getModel();

                Vector row = new Vector();
                row.add(ap.getProductoSleccionado().getCve());
                row.add(ap.getProductoSleccionado().getNombre());
                row.add(ap.getProductoSleccionado().getUnidaMedida());
                row.add(ap.getProductoSleccionado().getPrecio());
                row.add(ap.getProductoSleccionado().getCantidad());
                row.add(ap.getProductoSleccionado().getCantidad() * ap.getProductoSleccionado().getPrecio());

                model.addRow(row);
//            

                calculaTotales();
                tableDetalle.repaint();
                tableDetalle.revalidate();

            }
        });

        p = ap.showDialog();

        //  ap.setVisible(true);

    }//GEN-LAST:event_agregarProductoActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        // TODO add your handling code here:

        Object[] options = {"Si",
            "No"};
        int n = JOptionPane.showOptionDialog(this.rootPane,
                "Deseas eliminar el producto seleccionado?",
                "Confirma",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
        System.out.println("elimoinar: " + this.rowSeleccionado + "option: " + n);

        if (n == 0) {
            DefaultTableModel model = (DefaultTableModel) tableDetalle.getModel();
            model.removeRow(this.rowSeleccionado);
            this.botonEliminar.setEnabled(false);

        }
        calculaTotales();

    }//GEN-LAST:event_botonEliminarActionPerformed

